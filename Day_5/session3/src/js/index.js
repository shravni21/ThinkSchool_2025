// Strict equality
console.log(10==10);
// compares value as well as datatype
console.log(10===10);
console.log(10!==10);
console.log(10=='10')
console.log(10==='10')

// String Inequality
console.log(10!='10')

// Loose equality

// Doesn't compare datatype
console.log(10=='10')

// The good ones: === !==
// The Evil Twins: == !=

console.log(''=='0'); // false
console.log(0==''); // true 
console.log(false=='false'); // false
console.log(false=='0'); // true

console.log(true==1); // true
console.log(false==0); // true

console.log(true===1); // true
console.log(false===0); // true


// Logical operators

// OR ||
console.log(true||false); 
// AND &&
console.log(true&&false)
// NOT !
console.log(!true);
console.log(!false);






